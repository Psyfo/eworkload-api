import mongoose from 'mongoose';
import bcrypt from 'bcrypt-nodejs';
import validator from 'validator';
import transporter from './../config/mail';
import Position from './position';
import Discipline from './discipline';

const userSchema = new mongoose.Schema(
  {
    userId: {
      type: String,
      unique: true
    },
    password: {
      type: String
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      validate: value => {
        return validator.isEmail(value);
      }
    },
    firstName: {
      type: String
    },
    lastName: {
      type: String
    },
    photoUrl: {
      type: String
    },
    disciplineId: {
      type: String,
      ref: 'Discipline'
    },
    positionId: {
      type: String,
      ref: 'Position'
    },
    gender: {
      type: String
    },
    nationality: {
      type: String
    },
    workFocusName: {
      type: String,
      ref: 'WorkFocus'
    },
    createdAt: {
      type: Date
    },
    updatedAt: {
      type: Date
    }
  },
  {
    timestamps: true
  }
);

// VIRTUALS
userSchema.virtual('discipline', {
  ref: 'Discipline',
  localField: 'disciplineId',
  foreignField: 'disciplineId',
  justOne: true
});
userSchema.virtual('position', {
  ref: 'Position',
  localField: 'positionId',
  foreignField: 'positionId',
  justOne: true
});
userSchema.virtual('workFocus', {
  ref: 'WorkFocus',
  localField: 'workFocusName',
  foreignField: 'name',
  justOne: true
});
userSchema.virtual('full').get(function() {
  return (
    this.userId +
    '.' +
    this.email +
    '.' +
    this.firstName +
    '.' +
    this.lastName +
    '.' +
    this.photoUrl +
    '.' +
    this.disciplineId +
    '.' +
    this.positionId +
    '.' +
    this.gender +
    '.' +
    this.nationality
  );
});

// HOOKS
// Pre-hook to hash password. Make sure to use function and not arrow (lexical 'this' problem)
userSchema.pre('save', function(next) {
  const user = this;

  // Generate password and mail it
  user.password = Math.random()
    .toString(36)
    .slice(-8);

  const mailOptions = {
    from: 'c4mahlangu@gmail.com', // sender address
    to: user.email, // list of receivers
    subject: 'Eworkload Credentials', // Subject line
    html: `<p>Welcome to the Eworkload system. Please find default credentials below</p> 
        <p>User ID: ${user.userId}</p>
        <p>Password: ${user.password}</p>
        <br>
        <p>Autogenerated password may be changed from the user profile at any time.</p>
        <p>Kind regards</p>
        <p>Administrator</p>` // plain text body
  };
  transporter.sendMail(mailOptions, function(err, info) {
    if (err) console.log(err);
    else console.log(info);
  });

  // Generate salt and hash password
  bcrypt.genSalt(10, (err, salt) => {
    bcrypt.hash(user.password, salt, null, (err, hash) => {
      if (err) throw err;
      user.password = hash;
    });
  });

  // Indicates we're done and moves on to the next middleware
  next();
});

// INSTANCE METHODS
// Ensure correct password
userSchema.methods.isValidPassword = async password => {
  const user = this;

  // Hash sent password and compare with db hash
  const compare = await bcrypt.compare(password, user.password);
};

const User = mongoose.model('User', userSchema);
export default User;
